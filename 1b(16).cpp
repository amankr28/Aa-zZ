class Solution {
  public:
    void printTriangle(int n) {
        // code here
        char ch=64;
        for(int i=1;i<=n;i++)
        {
            ch++;
            for(int j=i;j>0;j--)
            {
                cout<<ch;
            }
            cout<<endl;
        }
    }
};

//{ Driver Code Starts.

int main() {
    int t;
    cin >> t;
    while (t--) {
        int n;
        cin >> n;

        Solution ob;
        ob.printTriangle(n);
    }
    return 0;
}
